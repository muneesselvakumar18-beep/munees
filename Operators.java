public class Operators {
    public static void main(String[] args) {
        int a=3;
        int i=5;
        System.out.println(3&5);
        i+=10;
        System.out.println(1<i  && i>20);
        System.out.println(1<i  || i>20);
        System.out.println(i++ + ++i);
        System.out.println(i-- - --i);
    }    
    
}
